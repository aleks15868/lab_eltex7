/**
 * @brief The addition of of two elements
 * 
 * @param [in] first_element Int, the first element for the mathematical operation of addition
 * @param [in] two_element Int, the second element for the mathematical operation of addition 
 * @return int (first_element / two_element)
 */
int add(int first_element, int two_element);
/**
 * @brief The Subtraction of two Elements
 * 
 * @param [in] first_element Int, the first element for the mathematical operation of addition
 * @param [in] two_element Int, the second element for the mathematical operation of addition 
 * @return int (first_element / two_element)
 */
int subtract(int first_element, int two_element);
/**
 * @brief The Multiplication of two Elements
 * 
 * @param [in] first_element Int, the first element for the mathematical operation of addition
 * @param [in] two_element Int, the second element for the mathematical operation of addition 
 * @return int (first_element / two_element)
 */
int multiply(int first_element, int two_element);
/**
 * @brief The division of two Elements
 * 
 * @param [in] first_element Int, the first element for the mathematical operation of addition
 * @param [in] two_element Int, the second element for the mathematical operation of addition 
 * @return float (first_element / two_element)
 */
float divide(int first_element, int two_element);